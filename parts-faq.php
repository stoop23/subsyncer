							<h4 class="faq_Question">How to resynchronize subtitles in VLC?</h4>
							<div class="faq_Answer">There are 3 ways to temporarily synchronize your subtitle in VLC media player.<br />
<b>Method 1:</b>
<ul>
	<li>Press G to add a 50 milliseconds delay</li>
	<li>Press H to remove 50 milliseconds delay (fasten)</li>
</ul>

<b>Method 2:</b> (<a href="https://superuser.com/questions/95760/how-can-i-re-sync-the-subtitle-and-the-video-using-vlc-media-player/811278#811278" target="_blank" rel="noopener">source</a>)
<ol>
	<li>Press Shift+H when you hear a specific sentence</li>
	<li>Press Shift+J when this sentence appears in the subtitles</li>
	<li>Press Shift+K to resynchronize the subtitles by the time difference between steps 1 and 2</li>
</ol>
You can also interchange Steps 1 and 2 if your subtitles are faster than the voices.<br /><br />

<b>Method 3:</b>
<ol>
	<li>In VLC, go to Tools > Track Synchronization</li>
	<li>In Subtitles/Video adjust the delay by clicking on the arrows up or down. You can fasten or delay the subtitles accordingly.</li>
</ol>
As those methods are not permanent, you can then use <a href="https://subsyncer.com">SubSyncer</a> to permanently fix your subtitles with the correct timings.</p>
